import {
  ExternalAuthors,
  Maybe,
  Sys,
  Users,
  gp2 as gp2Contentful,
} from '@asap-hub/contentful';
import { Asset } from '@asap-hub/contentful/src/crn/autogenerated-gql/graphql';
import { AuthorResponse } from '@asap-hub/model';

type QueryAuthor =
  | ({
      __typename: 'ExternalUsers';
    } & Pick<gp2Contentful.ExternalUsers, 'name'> & {
        sys: Pick<gp2Contentful.Sys, 'id'>;
      })
  | ({
      __typename: 'Users';
    } & Pick<
      gp2Contentful.Users,
      'firstName' | 'lastName' | 'onboarded' | 'email'
    > & {
        sys: Pick<gp2Contentful.Sys, 'id'>;
        avatar?:
          | gp2Contentful.Maybe<Pick<gp2Contentful.Asset, 'url'>>
          | undefined;
      })
  | ({
      __typename: 'ExternalAuthors';
    } & Pick<ExternalAuthors, 'name'>)
  | ({
      __typename: 'Users';
    } & Pick<
      Users,
      'alumniSinceDate' | 'firstName' | 'lastName' | 'onboarded'
    > & {
        sys: Pick<Sys, 'id'>;
        avatar?: Maybe<Pick<Asset, 'url'>> | undefined;
      });

export const getAuthor = (author: QueryAuthor): AuthorResponse => {
  if (author.__typename === 'Users') {
    return {
      user: {
        id: author.sys.id,
        firstName: author.firstName || '',
        lastName: author.lastName || '',
        displayName: `${author.firstName} ${author.lastName}`,
        avatarUrl: author.avatar?.url || undefined,
        alumniSinceDate: undefined,
      },
    };
  }

  return {
    externalUser: {
      displayName: author?.name || '',
    },
  };
};
